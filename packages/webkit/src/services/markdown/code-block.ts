export const escapeHtml = (text: string): string => {
  return text.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;')
}

const escapeHtmlAttribute = (text: string): string => {
  return escapeHtml(text).replace(/"/g, '&quot;').replace(/'/g, '&#39;')
}

const tokenColors = {
  comment: '#666666',
  string: '#FFE599',
  number: '#FED767',
  keyword: '#F3652B',
  builtin: '#6E7Cf7',
  punctuation: '#CCCCCC'
}

const wrap = (value: string, color: string): string =>
  `<span style="color:${color}">${value}</span>`

const highlightLine = (language: string, line: string): string => {
  const lang = (language || '').toLowerCase()
  const base = escapeHtml(line)

  const placeholders: string[] = []
  const save = (html: string): string => {
    const key = `@@TOK${placeholders.length}@@`
    placeholders.push(html)
    return key
  }
  const restore = (text: string): string =>
    text.replace(/@@TOK(\d+)@@/g, (_m: string, i: string) => placeholders[Number(i)] ?? _m)

  let text = base

  if (lang === 'bash' || lang === 'shell' || lang === 'sh') {
    text = text.replace(
      /(^|\s)(#[^$].*)$/g,
      (_m: string, prefix: string, comment: string) =>
        prefix + save(wrap(comment, tokenColors.comment))
    )
  } else {
    text = text.replace(/\/\/.*$/g, (m) => save(wrap(m, tokenColors.comment)))
    text = text.replace(/\/\*[\s\S]*?\*\//g, (m) => save(wrap(m, tokenColors.comment)))
  }

  text = text.replace(/`(?:\\`|[^`])*`/g, (m) => save(wrap(m, tokenColors.string)))
  text = text.replace(/'(?:\\'|[^'])*'/g, (m) => save(wrap(m, tokenColors.string)))
  text = text.replace(/"(?:\\"|[^"])*"/g, (m) => save(wrap(m, tokenColors.string)))

  text = text.replace(/\b\d+(?:\.\d+)?\b/g, (m) => save(wrap(m, tokenColors.number)))

  if (lang === 'json') {
    text = text.replace(/([{}[\]:,])/g, (m) => save(wrap(m, tokenColors.punctuation)))
  } else if (lang === 'javascript' || lang === 'typescript' || lang === 'js' || lang === 'ts') {
    const keywords = [
      'export',
      'default',
      'import',
      'from',
      'return',
      'const',
      'let',
      'var',
      'function',
      'class',
      'new',
      'if',
      'else',
      'for',
      'while',
      'switch',
      'case',
      'break',
      'continue',
      'try',
      'catch',
      'finally',
      'throw',
      'async',
      'await',
      'typeof',
      'instanceof',
      'in'
    ]
    const keywordRegex = new RegExp(`\\b(?:${keywords.join('|')})\\b`, 'g')
    text = text.replace(keywordRegex, (m) => save(wrap(m, tokenColors.keyword)))
    text = text.replace(/\b(?:true|false|null|undefined)\b/g, (m) =>
      save(wrap(m, tokenColors.builtin))
    )
    text = text.replace(/([{}[\]().,:;])/g, (m) => save(wrap(m, tokenColors.punctuation)))
  }

  return restore(text)
}

export const generateLineNumbers = (code: string): string => {
  const lines = code.split('\n')
  return lines
    .map((line, index) => {
      const lineNum = index + 1
      const escapedLine = highlightLine('text', line)
      return `<div class="flex"><span class="select-none text-neutral-600 text-right pr-4 min-w-[2.5rem]">${lineNum}</span><span class="flex-1">${escapedLine || ' '}</span></div>`
    })
    .join('')
}

export const getLanguageIcon = (language: string): string => {
  const lang = language.toLowerCase() as keyof typeof langIcons
  return langIcons[lang] || langIcons['bash']
}

